#include "so_long.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	char	*b;

	a = (char *)malloc(ft_strlen(s1) + 1);
	b = a;
	if (a == 0)
		return (0);
	while (*s1)
	{
		*a = *s1;
		a++;
		s1++;
	}
	*a = *s1;
	return (b);
}